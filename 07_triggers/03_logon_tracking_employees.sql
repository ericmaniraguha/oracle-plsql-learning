/*
TEST:
------
1. Log out and log back in as a user (or open a new session in SQL Developer/SQL*Plus).
2. Query the USER_LOG table to verify the log entry was recorded.

Example:

*/
SELECT * FROM USER_LOG
ORDER BY LOGON_TIME DESC;

/*
EXPECTED RESULT:
-----------------
You should see a row with:

- LOG_ID: auto-generated unique ID
- USERNAME: the user who just logged in
- SESSION_ID: session number assigned by Oracle
- LOGON_TIME: the exact timestamp of login
*/